def to_traditional(text):
    """简→繁：先做词级替换，再做字符级替换。"""
    if not text:
        return text
    out = text
    # 词级替换（长词优先，避免短词破坏长词）
    for src in sorted(WORD_MAP.keys(), key=len, reverse=True):
        if src in out:
            out = out.replace(src, WORD_MAP[src])
    # 字符级替换
    out = "".join(CHAR_MAP.get(c, c) for c in out)
    return out


